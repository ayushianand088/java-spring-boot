public class BitwiseOperators {
    public static void main(String[] args) {
        int num1 = 4;
        int num2 = 6;

        System.out.println("num1 is " + num1);
        System.out.println("num2 is " + num2);

        System.out.println("num1 & num2 = " + (num1 & num2));
        System.out.println("num1 | num2 = " + (num1 | num2));
        System.out.println("num1 ^ num2 = " + (num1 ^ num2));
        System.out.println("~num1 = " + (~num1));
    }
}
